/**
 * @brief
 *	Load node mominfo_time from the database
 *
 * @param[in]	conn - Connection handle
 * @param[in]	obj  - Load node information into this object
 *
 * @return      Error code
 * @retval	-1 - Failure
 * @retval	 0 - Success
 * @retval	 1 -  Success but no rows loaded
 *
 */
int
pbs_db_load_mominfo_tm(void *conn, pbs_db_obj_info_t *obj)
{
	pbs_db_mominfo_time_t *pmi = obj->pbs_db_un.pbs_db_mominfo_tm;
	static int mit_time_fnum = -1;
	static int mit_gen_fnum = -1;
	as_key key;
	as_error err;
	as_record* p_rec = NULL;
	aerospike *as = (aerospike *) conn;
	int rc;

	as_key_init_str(&key, g_namespace, "mominfo_time", "mominfo_time");
	rc = aerospike_key_get(as, &err, NULL, &key, &p_rec);
	if (rc != AEROSPIKE_OK) {
		if (rc == AEROSPIKE_ERR_RECORD_NOT_FOUND) {
			mit_time_fnum = pmi->mit_time;
			mit_gen_fnum = pmi->mit_gen;
			as_record_destroy(p_rec);
			return 1;
		}
		char msg[4096];
		sprintf(msg, "aerospike_key_get() returned %d - %s\n", err.code, err.message);
		db_set_error("Execution of load mominfo_time", msg, NULL);
		as_record_destroy(p_rec);
		return -1;
	}

	if (mit_time_fnum == -1 || mit_gen_fnum == -1) {
		mit_time_fnum = as_record_get_int64(p_rec, "mit_time", INT64_MAX);
		mit_gen_fnum = as_record_get_int64(p_rec, "mit_gen", INT64_MAX);
	}
	pmi->mit_time = mit_time_fnum;
	pmi->mit_gen = mit_gen_fnum;
	as_record_destroy(p_rec);

	return 0;
}
